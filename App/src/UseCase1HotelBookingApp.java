/**
 * UseCase1HotelBookingApp
 *
 * This class represents the entry point of the Hotel Booking Application.
 * It demonstrates how a Java program starts execution using the main() method
 * and displays a welcome message to the console.
 *
 * The program prints the application name and version information
 * when executed.
 *
 * @author Prathik
 * @version 1.0
 */

public class UseCase1HotelBookingApp {

    /**
     * The main method is the entry point of the Java application.
     * The JVM invokes this method when the program is executed.
     *
     * @param args Command line arguments
     */
    public static void main(String[] args) {

        // Display application header
        System.out.println("=================================");
        System.out.println("       Hotel Booking System      ");
        System.out.println("            Version 1.0          ");
        System.out.println("=================================");

        // Welcome message
        System.out.println("Welcome to the Hotel Booking Application!");
        System.out.println("The system has started successfully.");
    }
}